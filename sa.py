First_Number=int(input("Enter First Number : "));
Second_Number=int(input("Enter Second Number : "));
Operator=input("Give Operator: ");
if Operator=="+":
    print(First_Number+Second_Number)
elif Operator=="-":
    print(First_Number-Second_Number);
elif Operator == "*":
    print(First_Number*Second_Number)
elif Operator=="/":
    print(First_Number/Second_Number)
else:
    print("Invalid Operator")
